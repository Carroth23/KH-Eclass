public class Exam_01_Concept { // 클래스 이름은 파일명과 똑같이
	public static void main(String[] args) { // 시작지점 (main은 시작커맨드)
		
		// () <-- 소괄호 , {} <-- 중괄호, [] <-- 대괄호
		
		System.out.println("\"Hello World\""); // \는 이스케이프문자
		System.out.println(100);
		// 이 코드는 100을 숫자로 출력하는 코드
		// 주석은 컴파일에서 모두 떨어져나감.
		
		System.out.println('A');
		System.out.println(3.14);
		// ctrl+space : code Assistant
	}
}


